element= input("2A grubundaki elementlerden birisini yazınız:")
if element == 'berilyum':
    print("Sembolü: Be, Numarası:4")
elif element == 'magnezyum':
    print("Sembolü: Mg, Numarası:12")
elif element == 'kalsiyum':
    print("Sembolü:Ca, Numarası:20")
elif element == 'stronsiyum':
    print("Sembolü:Sr, Numarası:38")
elif element == 'baryum':
    print("Sembolü: Ba, Numarası:56")
elif element == 'radyum':
    print("Sembolü: Ra, Numarası:88")
else:
    print("Girdiğiniz element 2A grubunda bulunmamaktadır.")